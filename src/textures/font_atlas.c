/* Font atlas texture — a crisp 8x12 monospace bitmap font baked into a
 * texture so shaders can render legible text by sampling, instead of the
 * cramped hand-rolled 3x5 tables shaders usually embed.
 *
 * Layout: a 16-column x 6-row grid of cells, each cell 8x12 px.
 *   atlas size = 128 x 72 px  (16*8 x 6*12)
 * Cell index = ascii - 32, so printable ASCII 32..127 maps row-major:
 *   row = index / 16, col = index % 16.
 *
 * Shader usage (see nwFontGlyph in shader_stdlib):
 *   given an ascii code and a 0..1 local cell coordinate, sample the atlas
 *   and read the .r channel (1 = ink, 0 = paper). GL_NEAREST keeps it crisp.
 */

